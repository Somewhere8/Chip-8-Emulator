void loadFont(const uint8_t *font, size_t size) {
    for(size_t i = 0; i < size; i++)
        mem[i] = font[i];
}

void loadROM(uint8_t *rom, size_t size) {
    for(size_t i = 0; i < size; i++)
        mem[0x200 + i] = rom[i];
}